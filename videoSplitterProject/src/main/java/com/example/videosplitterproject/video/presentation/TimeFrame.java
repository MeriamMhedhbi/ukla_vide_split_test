package com.example.videosplitterproject.video.presentation;

public class TimeFrame {
    private int start;
    private int end;

    // Getters and setters
    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}

